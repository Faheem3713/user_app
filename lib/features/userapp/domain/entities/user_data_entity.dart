class UserData {
  final int id;
  final String email;
  final String firstName;
  final String lastName;
  final String avatar;
  final String url;
  final String text;

  UserData({
    required this.id,
    required this.email,
    required this.firstName,
    required this.lastName,
    required this.avatar,
    required this.url,
    required this.text,
  });
}
